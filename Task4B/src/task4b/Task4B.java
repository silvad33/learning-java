public class Task4B {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) 
    {
        int lp = 10; // Initialize lp to 1
        while ( lp >= 1 )// Execute loop while lp less than or equal to 10
        {
            System.out.print( lp + " " );
            lp--; // Increase the lp value
        }
        System.out.print( "\n" );
    }
    
}
